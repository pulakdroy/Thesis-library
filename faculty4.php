    <div id="biography" class="profile-section">
        <h2>Biography</h2>
        <p>Dr. Muhammad Iqbal Hossain is an Associate Professor of the Computer Science and Engineering Department of BRAC University. He received a Ph.D. degree (Computer Engineering) from Kyungpook National University, South Korea in January 2018. His academic journey is marked by significant achievements and rigorous pursuits. Dr. Hossain earned his Ph.D. in Computer Engineering from Kyungpook National University, South Korea, in January 2018. During his doctoral tenure, he actively participated in the Embedded Software Engineering Lab. His contributions to this lab resulted in a substantial collection of peer-reviewed journals and presentations at both international and domestic conferences in Korea. Dr. Hossain's commitment to staying at the forefront of his field is evident through his active participation in various international conferences and symposiums, which have enriched his perspectives and insights.

            Dr. Hossain's scholarly pursuits have garnered him recognition beyond his academic accomplishments. His expertise has led him to assume significant roles as a Reviewer and Editorial Board Member for reputable international journals and conferences. Moreover, his remarkable academic journey was facilitated by prestigious scholarships, including the Korean Government Scholarship (GKS) in 2009, which enabled his graduate studies. Subsequently, his exceptional dedication and contributions were acknowledged with the KNU Honors Scholarship during his doctoral pursuit.
            
            Dr. Iqbal’s Research interests are mainly focused on Information security (Cryptography, Access control, privacy etc), AI and Machine learning, Model-based Software Engineering, Software Testing and Verification, Automation etc.</p>
