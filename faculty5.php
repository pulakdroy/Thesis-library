        <h2>Biography</h2>
        <p>Md. Tawhid Anwar is currently working as a Senior Lecturer in the Computer Science and Engineering department of BRAC University. Prior to this, he served as an Undergraduate Teaching Assistant (aka Student Tutor) in the Department of CSE for almost 2 years. In addition, he was also a Student Mentor, where his responsibility was to guide fresher students in their academic improvement. Tawhid completed his M.Sc. in Computer Science and Engineering from BRAC University with an outstanding academic record. He is a dedicated, hardworking, and decisive individual. He completed his B.Sc. in Computer Science and Engineering from BRAC University.

 

            Tawhid oversees the BRAC University Entrepreneurship Development Forum - BUEDF club as the faculty co-advisor. He is well known for his leadership skills in managing a team noted for its high level of collaboration. He strives to contribute to the greater benefit of students through knowledge and wisdom. His research interests include Artificial Intelligence, Financial Market Analysis, and Machine Learning.</p>
